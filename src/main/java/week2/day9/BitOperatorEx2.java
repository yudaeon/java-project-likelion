package week2.day9;

public class BitOperatorEx2 {
    public static void main(String[] args) {
        int val1 = 1;
         System.out.println(~val1); //-2
        System.out.println(Integer.toBinaryString(val1)); // 1
        System.out.println(Integer.toBinaryString(~val1)); //111111111110
    }
}
