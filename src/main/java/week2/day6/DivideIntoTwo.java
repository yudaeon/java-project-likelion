package week2.day6;

public class DivideIntoTwo {
    public static void main(String[] args) {
        // 1 + 1 /2
        float fOne = 1 ;
        float fTwo = 2;
        System.out.printf("%f\n", fOne + fOne / fTwo ); //1.5

        int iOne = 1;
        int iTwo = 2;
        System.out.printf("%d\n", iOne + iOne / iTwo); //1출력

        System.out.printf("%d\n", 9/2);
        System.out.printf("%f\n", 9/2.0);

        // float 뒤에 소수점을 작성할 때에는 f를 뒤에 붙여야함
    }
}
