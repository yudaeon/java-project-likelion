package week2.day3;

public class ArithmeticOperators1 {
    public static void main(String[] args) {
        System.out.printf("a + b = %d\n", 3 + 10); //13
        System.out.printf("a - b = %d\n", 3 - 10); //-7
        System.out.printf("a * b = %d\n", 3 * 10); //30
        System.out.printf("a / b = %d\n", 3 / 10); //0
        System.out.printf("a %% b = %d\n", 3 % 10); //1
    }
}
